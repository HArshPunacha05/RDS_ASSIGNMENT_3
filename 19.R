# Define two strings
string1 <- "apple"
string2 <- "Banana"

# Compare the strings using relational operators
is_less_than <- string1 < string2
is_equal_to <- string1 == string2
is_greater_than <- string1 > string2

# Print the results
cat("Is string1 less than string2? ", is_less_than, "\n")
cat("Is string1 equal to string2? ", is_equal_to, "\n")
cat("Is string1 greater than string2? ", is_greater_than, "\n")

#OUTPUT
#Is string1 less than string2?  TRUE 
#Is string1 equal to string2?  FALSE 
#Is string1 greater than string2?  FALSE 