# Example numeric vector
numeric_vector <- c(5, 12, 8, 15, 3, 20, 9)

# Extract elements greater than 10 using logical subsetting
elements_greater_than_10 <- numeric_vector[numeric_vector > 10]

# Print the result
cat("Elements greater than 10:", elements_greater_than_10, "\n")


#OUTPUT
#Elements greater than 10: 12 15 20 